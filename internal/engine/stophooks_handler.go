package engine

import (
	"context"
	"log/slog"
	"strings"
)

// ────────────────────────────────────────────────────────────────────────────
// Stop hooks handler — orchestrates the full stop_reason hook pipeline.
// Aligned with claude-code-main query/stopHooks.ts handleStopReason.
//
// The pipeline:
//   1. Run user-defined stop hooks (StopHookExecutor)
//   2. Run post-sampling hooks (memory extraction, auto-dream, cleanup)
//   3. Evaluate blocking errors from (1)
//   4. Return decision: terminal vs continue
// ────────────────────────────────────────────────────────────────────────────

// StopReasonDecision is the outcome of evaluating stop hooks.
type StopReasonDecision struct {
	// ShouldContinue is true if the model should continue (e.g. hook blocked stop).
	ShouldContinue bool
	// BlockingMessage is the user message to inject if a hook blocked the stop.
	BlockingMessage *Message
	// IsTerminal is true if the loop should exit.
	IsTerminal bool
	// TerminalReason is set when IsTerminal is true.
	TerminalReason TerminalReason
	// PostSamplingMessages are system messages generated by post-sampling hooks.
	PostSamplingMessages []*Message
	// MemoryExtracted is true if memory extraction was triggered.
	MemoryExtracted bool
}

// StopHooksHandler orchestrates the full stop hook pipeline.
type StopHooksHandler struct {
	// executor runs user-defined shell hooks.
	executor *StopHookExecutor
	// postSamplingHooks are built-in hooks that run after the model stops.
	postSamplingHooks []PostSamplingHook
	// maxBlockingRetries caps how many times blocking hooks can force a retry.
	maxBlockingRetries int
	// blockingRetryCount tracks current retry count.
	blockingRetryCount int
}

// PostSamplingHook is a built-in hook that runs after the model produces output.
// Examples: memory extraction, auto-dream, prompt suggestion.
type PostSamplingHook interface {
	// Name returns the hook's identifier.
	Name() string
	// ShouldRun returns true if this hook should execute for the given context.
	ShouldRun(ctx *StopHookContext) bool
	// Run executes the hook and returns any messages to inject.
	Run(ctx context.Context, hookCtx *StopHookContext) (*PostSamplingResult, error)
}

// PostSamplingResult is the output of a post-sampling hook.
type PostSamplingResult struct {
	// Messages are system messages to inject into the conversation.
	Messages []*Message
	// MemoryExtracted is true if this hook extracted memory.
	MemoryExtracted bool
}

// StopHookContext carries the context for stop hook evaluation.
type StopHookContext struct {
	// StopReason is the model's stop_reason.
	StopReason string
	// AssistantMessage is the model's last response.
	AssistantMessage *Message
	// Messages is the full conversation history.
	Messages []*Message
	// TurnCount is the current turn number.
	TurnCount int
	// Model is the model name.
	Model string
	// SessionID is the session identifier.
	SessionID string
	// HasToolUse is true if the assistant message contains tool_use blocks.
	HasToolUse bool
	// QuerySource is the origin of the current query.
	QuerySource QuerySource
}

// NewStopHooksHandler creates a handler with the given executor and hooks.
func NewStopHooksHandler(executor *StopHookExecutor, postHooks []PostSamplingHook) *StopHooksHandler {
	return &StopHooksHandler{
		executor:           executor,
		postSamplingHooks:  postHooks,
		maxBlockingRetries: 3,
	}
}

// HandleStopReason evaluates the full stop hook pipeline.
// Aligned with claude-code-main query/stopHooks.ts handleStopReason.
func (h *StopHooksHandler) HandleStopReason(ctx context.Context, hookCtx *StopHookContext) *StopReasonDecision {
	decision := &StopReasonDecision{}

	// Skip hooks for tool_use stop reasons — the loop continues to execute tools.
	if hookCtx.HasToolUse {
		return decision
	}

	// ── 1. Run user-defined stop hooks ───────────────────────────────────
	if h.executor != nil {
		env := buildStopHookEnv(hookCtx)
		result := h.executor.Run(ctx, StopHookOnStop, env)
		if result != nil && !result.Continue {
			// Hook blocked the stop.
			if h.blockingRetryCount >= h.maxBlockingRetries {
				slog.Warn("stophooks: max blocking retries reached",
					slog.Int("retries", h.blockingRetryCount))
				decision.IsTerminal = true
				decision.TerminalReason = TerminalStopHookPrevented
				return decision
			}
			h.blockingRetryCount++

			slog.Info("stophooks: hook blocked stop",
				slog.String("command", result.Command),
				slog.Int("exit_code", result.ExitCode),
				slog.Int("retry", h.blockingRetryCount))

			decision.ShouldContinue = true
			decision.BlockingMessage = buildBlockingMessage(result)
			return decision
		}
	}

	// ── 2. Run post-sampling hooks ───────────────────────────────────────
	for _, hook := range h.postSamplingHooks {
		if !hook.ShouldRun(hookCtx) {
			continue
		}
		result, err := hook.Run(ctx, hookCtx)
		if err != nil {
			slog.Warn("stophooks: post-sampling hook failed",
				slog.String("hook", hook.Name()),
				slog.Any("err", err))
			continue
		}
		if result != nil {
			decision.PostSamplingMessages = append(decision.PostSamplingMessages, result.Messages...)
			if result.MemoryExtracted {
				decision.MemoryExtracted = true
			}
		}
	}

	// ── 3. Normal terminal ───────────────────────────────────────────────
	decision.IsTerminal = true
	decision.TerminalReason = TerminalCompleted

	// Reset blocking retry count on successful stop.
	h.blockingRetryCount = 0

	return decision
}

// ResetBlockingCount resets the blocking retry counter (between queries).
func (h *StopHooksHandler) ResetBlockingCount() {
	h.blockingRetryCount = 0
}

// ── Built-in post-sampling hooks ─────────────────────────────────────────

// MemoryExtractionHook extracts memories from the conversation.
// Aligned with claude-code-main stopHooks.ts extractMemory.
type MemoryExtractionHook struct {
	// Extractor is the function that extracts memories.
	Extractor func(ctx context.Context, messages []*Message, model string) ([]string, error)
}

func (h *MemoryExtractionHook) Name() string { return "memory_extraction" }

func (h *MemoryExtractionHook) ShouldRun(ctx *StopHookContext) bool {
	// Only run on main thread queries, not compaction or subagent.
	return ctx.QuerySource.IsMainThread() && ctx.StopReason == "end_turn"
}

func (h *MemoryExtractionHook) Run(ctx context.Context, hookCtx *StopHookContext) (*PostSamplingResult, error) {
	if h.Extractor == nil {
		return nil, nil
	}
	memories, err := h.Extractor(ctx, hookCtx.Messages, hookCtx.Model)
	if err != nil {
		return nil, err
	}
	if len(memories) == 0 {
		return &PostSamplingResult{}, nil
	}
	slog.Info("stophooks: extracted memories", slog.Int("count", len(memories)))
	return &PostSamplingResult{MemoryExtracted: true}, nil
}

// PromptSuggestionHook generates follow-up prompt suggestions.
// Aligned with claude-code-main stopHooks.ts suggestPrompts.
type PromptSuggestionHook struct {
	// Suggest is the function that generates suggestions.
	Suggest func(ctx context.Context, messages []*Message, model string) ([]string, error)
}

func (h *PromptSuggestionHook) Name() string { return "prompt_suggestion" }

func (h *PromptSuggestionHook) ShouldRun(ctx *StopHookContext) bool {
	return ctx.QuerySource.IsMainThread() && ctx.StopReason == "end_turn"
}

func (h *PromptSuggestionHook) Run(ctx context.Context, hookCtx *StopHookContext) (*PostSamplingResult, error) {
	if h.Suggest == nil {
		return nil, nil
	}
	suggestions, err := h.Suggest(ctx, hookCtx.Messages, hookCtx.Model)
	if err != nil {
		return nil, err
	}
	if len(suggestions) == 0 {
		return &PostSamplingResult{}, nil
	}
	slog.Debug("stophooks: generated prompt suggestions", slog.Int("count", len(suggestions)))
	return &PostSamplingResult{}, nil
}

// ── Helpers ──────────────────────────────────────────────────────────────

// buildStopHookEnv builds environment variables for stop hook shell commands.
func buildStopHookEnv(ctx *StopHookContext) []string {
	env := []string{
		"STOP_REASON=" + ctx.StopReason,
		"TURN_COUNT=" + intToStr(ctx.TurnCount),
		"MODEL=" + ctx.Model,
		"SESSION_ID=" + ctx.SessionID,
	}
	// Extract last assistant text for the hook.
	if ctx.AssistantMessage != nil {
		for _, b := range ctx.AssistantMessage.Content {
			if b.Type == ContentTypeText && b.Text != "" {
				text := b.Text
				if len(text) > 4096 {
					text = text[:4096]
				}
				env = append(env, "ASSISTANT_MESSAGE="+text)
				break
			}
		}
	}
	return env
}

// buildBlockingMessage creates a user message injected when a hook blocks the stop.
func buildBlockingMessage(result *StopHookResult) *Message {
	reason := strings.TrimSpace(result.Stderr)
	if reason == "" {
		reason = strings.TrimSpace(result.Stdout)
	}
	if reason == "" {
		reason = "Stop hook failed with exit code " + intToStr(result.ExitCode)
	}
	return &Message{
		Role:   RoleUser,
		Type:   MsgTypeUser,
		IsMeta: true,
		Content: []*ContentBlock{{
			Type: ContentTypeText,
			Text: "Stop hook failed: " + reason + ". Please address this before completing.",
		}},
	}
}

// intToStr is a simple int-to-string helper to avoid importing strconv.
func intToStr(n int) string {
	if n == 0 {
		return "0"
	}
	neg := false
	if n < 0 {
		neg = true
		n = -n
	}
	var buf [20]byte
	i := len(buf)
	for n > 0 {
		i--
		buf[i] = byte('0' + n%10)
		n /= 10
	}
	if neg {
		i--
		buf[i] = '-'
	}
	return string(buf[i:])
}
