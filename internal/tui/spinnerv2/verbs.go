// RandomVerb returns a random spinner verb in the current language.
func RandomVerb() string {
	switch currentLang {
	case LangEN:
		return SpinnerVerbsEN[rand.Intn(len(SpinnerVerbsEN))]
	default: // LangCN
		return SpinnerVerbsCN[rand.Intn(len(SpinnerVerbsCN))]
	}
}
